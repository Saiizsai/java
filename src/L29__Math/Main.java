package L29__Math;

public class Main {
    public static void main(String[] args) {

        int val = 8;
        int value = -10;
        //abs ka bl 2 lone kwar ll so tr pya
        System.out.println(Math.abs(val));
        System.out.println(Math.abs(value));


        double d = 10.4;
        //ceil ka a por ko kyi
        System.out.println(Math.ceil(d));


        //floor ka out ka kg ko kyi
        System.out.println(Math.floor(d));


        int a = 140194194;
        int b = 24348923;
        //max ka kyi tk kg ko pya
        System.out.println(Math.max(a,b));


        //min ka a ngl sone kg ko pya
        System.out.println(Math.min(a,b));


        int num = 2;
        int power = 3;
        //pow ka power
        System.out.println(Math.pow(num , power));


        //random
        System.out.println(Math.random());


        double e = 20.4;
        double f = 20.5;
        //round a nee sat sone kg ko u
        System.out.println(Math.round(e));
        System.out.println(Math.round(f));
    }
}
