package L19__Custom_Wrapper;

public class wrapper_class {
    public static void main(String[] args) {

        //auto boxing     sar thr pyg
        //primitive data type >>> obj
        Integer y = 20;
        System.out.println(y.toString()+ 4);

        //unboxing        sar thr ka ny value pyg
        int t = y;
        System.out.println(t+ 4);

        Integer i = 29;
        System.out.println(i + 1);

    }
}
